package com.controller.model;
/**
 * This model class for item which is a candidate for sorting
 * @author Tayab Hussain
 * @version 1.0
 */
public class SortItem {
	String id;
	String value;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getValue() {
		return value;
	}
	public void setValue(String value) {
		this.value = value;
	}
	
	
}
